export const getTranslation = (lang: string, key: string, defaultValue = ''): string => {
  try {
    const parts = key.split('.');
    let value: any = (translations as any)[lang] || (translations as any).vi;
    
    for (const part of parts) {
      value = value[part];
      if (!value) return defaultValue;
    }
    
    return typeof value === 'string' ? value : defaultValue;
  } catch {
    return defaultValue;
  }
};
